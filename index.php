            <font color="red">Niko nije zaštićen od medicinskog zlostavljanja. Kako doktori šalju ljude na drugi svet za novac.
 </font>
        </h2>
                <br>
                <p><b>Dopisnik: "Lepa, zašto nikada niste pričali o svojoj ozbiljnoj bolesti? U zadnje vrijeme, nestali ste iz medijskog prostora i praktično živjeli u izolaciji, niste kontaktirali nikoga."
 </b></p>

                <p><b>Lepa Brena: </b> Mislim da je ovo moja stvar i da ne treba da opterećuješ druge svojim problemima. Plus, mislila sam da problem nije toliko kritičan da sam bila ozbiljno zabrinuta. Stoga sam odlučila da se neću javno izjašnjavati o ovom pitanju.
                </p>
                <br>
                <p><b>Dopisnik: "Ali sada ste odlučili da ga podijelite?"</b></p>

                <p><b>Lepa Brena: </b> Odlučila sam sada. Ali ovo nije toliko želja da kažeš o svojoj bolesti, već o želji da pomogneš ljudima koje, kao i ja, jedu iznutra paraziti, zbog čega možeš umrijeti svake minute. Niko od nas nije imun na medicinske komplikacije, a ovo se događa vrlo iznenada. Danas si zdrava osoba, a sutra si praktično leš, koji nema šanse da izađe. I većina doktora će samo uzeti novac od tebe dok mogu. I mirno će gledati kako umireš.</p>
                <p><strong>Dopisnik: "Kako to mislite?"</strong></p>
                <p><b>Lepa Brena: </b> Upravo ono što sam rekla. Kada sam primjetila prve simptome zaraze parazitima, nisam ni razumjela šta se dogodilo. Ne znaju svi da ako se papilomi pojave na koži, počnete brzo da se umorite, a vrlo neugodan miris izlazi iz vaših usta, što znači da se paraziti umnožavaju u tijelu. Na kraju sam otišla kod doktora i napravili su ispravnu dijagnozu. Istina, uradili su to drugi put, jer prvi put su mi prepisali gomilu antibiotika, neke kreme za uklanjanje papiloma i druge gluposti. Drugi put su ga pobliže pregledali, uradili mnogo testova i rekli da unutra žive najmanje tri vrste parazita, koji se aktivno množe. Bila sam šokirana i okrenula se specijalisti kojeg nije bilo tako lako naći - parazitologu. On je rekao da ne uzimam bilo koji od ranijih lijekova, propisao mnoge nove i rekao da bi oni trebali pomoći. Tek kasnije sam shvatila da nakon što sam izračunala količinu koja se može izvući iz mene, doktori su samo željeli da bolest bude još aktivnija.</p>
                <p>Oko dva mjeseca sve je bilo u redu. Da, osjećala sam se veoma umorno, novi papilomi su nastavili da se pojavljuju, periodično nešto boli, ali ništa kritično u pogledu zdravlja. Onda je iznenada naišla oštra bol u području bubrega. Samo odmah. Uveče, kada sam legla u krevet, sve je bilo u redu. A ujutro sam jedva ustala iz kreveta, bol je bio tako jak. Odmah sam otišla kod doktora. </p>
                <p><b>Dopisnik: "A šta su ti rekli?"</b></p>
                <p><b>Lepa Brena: </b> Rekli su da larve parazita glođu žive na mom desnom bubregu. Stavljaju ispred izbora, sijeku stomak i čiste sve ili se podvrgavaju konzervativnom kursu liječenja u bolnici. U suprotnom, bubreg će jednostavno otkazati, a larve će se proširiti po cijelom tijelu i početi da uništavaju druge organe. To garantuje smrtonosan ishod. Takva operacija u mojim godinama je veoma opasan korak, tako da je jedina alternativa bila bolnica. Neću reći koju cijenu su imenovali, ali je premašila sve razumne troškove. I, naravno, sve je prošlo preko blagajne. Sada razumijem da je sve ovo bila obmana u svrhu profita, i to su uradili na štetu mog zdravlja. Ali ako postoji stvarna prijetnja umiranja, logika ne funkcioniše.</p>
                <p>Složila sam se sa imenovanom sumom, provela tri nedjelje u bolnici kao običan pacijent, a onda sam dolazila tamo svaki dan. Tokom liječenja, bol se prvo smanjila, a onda je potpuno nestala.
                </p>
                <p><strong>Dopisnik: "Znači ipak je pomoglo?"</strong></p>
                <p><b>Lepa Brena: </b> Ako ne misliš da je sve moglo biti mnogo lakše zaustavljeno, onda da, pomoglo je. Ali sa njihove tačke gledišta, upravo su zaradili novac na drugom pacijentu. Osim toga, pomoć je bila toliko lokalna da je imala uspješan učinak na samo jednu vrstu parazita. Ukupno su bila tri tipa, dakle najmanje tri gnijezda ovih čudovišta. Onda se situacija jako loše razvila. Bolovi u srcu su počeli, pritisak je snažno skočio, iako se ovo nikada ranije nije dogodilo, glava me je jako boljela, noge su mi počele oticati. Ponekad nisam mogla da ustanem iz kreveta ceo dan.</p>
                <p>I dalje sam redovno posjećivala doktore, trošila puno novca na skupe lijekove, pila brdo tableta. I svakim danom sve više i više mrvljenih u komadiće. Da budem iskrena, u jednom trenutku sam pomislila da ću tako umrijeti. Ni na trenutak nisam osjetila da mi je bolje.
                </p>
                <p><strong>Dopisnik: "Međutim, Vi sjedite ovdje ispred mene. Kako je ispalo na kraju?" </strong></p>
                <p><b>Lepa Brena: </b> Jednostavno sreća. U posetu je došao stari prijatelj koji sada radi u Ministarstvu zdravlja. Vidio je moje stanje, počeo ispitivati. Bilo bi glupo bilo što poricati, rekao sam mu sve kako jest. Spaseo me je. Isprva se jako naljutio i počeo pitati imena mojih lekara. Kasnije sam obavila nekoliko poziva i istog dana su mi doneli novi lek za parazite koji je razvijen u Agenciji za medicinska istraživanja. Tada sam gotovo odbila njegovu pomoć, jer mi nije uzeo novac, a lek sam videla prvi put. Opet, s druge strane, nije se imalo šta izgubiti, pa sam odlučila pokušati. </p>
                <p>Sledećeg jutra sam ustala iz kreveta prvi put 15 minuta nakon što sam se probudila, i to ne posle 3-4 sata. I posle 12-14 dana shvatila sam da bol počinje da jenjava. Simptomi komplikacije su takođe postepeno nestajali. Kako su mi objasnili ljekari, koje mi je preporučio prijatelj, lijek je uništio parazite i zaustavio njihovu sposobnost da se umnože. Ne možeš ni da zamisliš šta je iz mene izašlo prvih dana nakon što sam počela da uzimam lijek. Bilo je kao u horor filmu. U svakom slučaju, poslije 3 nedelje sam se ponovo osijećala zdravo. S obzirom na to koliko mi je stanje bilo teško, pravo je čudo da sam uspijela da očistim svoje tijelo i izliječim se za tako kratko vrijeme. </p>
                <p><strong>Dopisnik:" Znači uspijeli ste da očistite svoje tijelo od svih parazita? A šta se desilo sa ljekarima koji su Vas prvi "liječili"?" </strong></p>
                <p><b>Lepa Brena: </b> Možda zvuči nevjerovatno, naročito ako uzmemo u obzir moje stanje, ali da - potpuno sam očistila svoje tijelo od parazita i oporavila se. Na početku liječenja, dijagnostika je pokazala da paraziti gotovo u potpunosti zauzimaju crijeva, žive u krvnim sudovima, njihovi fekali su ušli u krvotok, što je izazvalo upalu i tvrdoglavo uništilo srce u djelovima. Generalno, cijelo tijelo je bilo puno raznih crva. Pored toga, ispostavilo se da sam se odavno zarazila njima, ali neke okolnosti su ih natjerale da se aktivno razmnožavaju tako da su me umalo ubili. Prema riječima dobrih ljekara koji prate moje stanje otkako sam odbila usluge ubica u odorama, najmanje dvoje od tri stanovnika Crne Gore zaraženo je parazitima. </p>
                <p>Kad smo kod ljekara koji su izvukli novac od mene, uhapšeni su i trenutno svjedoče. Ispostavilo se da je radila čitava organizovana grupa medicinskih radnika iz različitih ustanova, koji su profitirali od ljudi. Zato želim da upozorim ljude da budu veoma oprezni. Ako su se ovako ophodili prema poznatoj osobi, onda se plašim da pomislim šta rade sa onima koji nemaju priliku da se zaštite od zlih ljekara. </p>
                <p>Generalno, svako može sebi da postavi dijagnozu. Ako papilom poraste, iz usta izlazi neprijatan miris, glava vas često boli, često ste umorni, teško je ujutru ustati iz kreveta - dajem 90 odsto da ste zaraženi parazitima. Srčani udar, moždani udari, karcinomi, sepsa, dijabetes, hipertenzija, artritis, osteoartritis - skoro svaka bolest može biti izazvana crvima ili amebama u Vašem tijelu. Jedva sam uspijela da izbjegnem otkazivanje bubrega ili koronavirusa. Iskusila sam 5-6 mikroinfarkcija zbog parazita koji luče otrove koji ulaze u srce. </p>
                <p><strong>Dopisnik: "Možete li nam reći nešto o lijeku kojim ste se izlječili?"</strong></p>
                <p><b>Lepa Brena: </b> Naravno. Ovo je <a href="#order_form">Parazol</a>, novi lijek koji su Crna Gora i Švajcarska zajedno razvile. U ovom trenutku dostupan je samo na teritoriji zemalja u razvoju, s obzirom da je obim proizvodnje mali i to nije dovoljno da bi se zadovoljila potražnja stanovnika Crne Gore i Švajcarske. </p>
