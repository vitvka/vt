                <p>Sasvim nedavno, Ministarstvo zdravlja je shvatilo kako da se nosi sa ovom situacijom. Napravili su poseban obrazac za porudžbinu, svaki stanovnik zemlje može da pošalje zahtjev. Sada nema birokratije u ovom procesu. Potrebno je samo da naznačite svoje ime i broj telefona kako bi vas operater državnog programa kontaktirao, konsultovao i naveo kada bi bilo najpogodnije da dostavite lijek. </p>

